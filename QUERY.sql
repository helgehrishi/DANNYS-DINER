-- 09 IF EACH $1 SPENT EQUATES TO 10 POINTS AND SUSHI HAS A 2X POINTS MULTIPLIER - HOW MANY POINTS WOULD EACH CUSTOMER HAVE?
-- RETRIEVE CUSTOMER ID, PRODUCT NAME, AND TOTAL AMOUNT SPENT BY EACH CUSTOMER ON EACH PRODUCT
SELECT
    S.CUSTOMER_ID,
    M.PRODUCT_NAME,
    SUM(M.PRICE) AS AMOUNT_SPENT
FROM
    SALES AS S
    INNER JOIN MENU AS M ON S.PRODUCT_ID = M.PRODUCT_ID
GROUP BY
    S.CUSTOMER_ID,
    M.PRODUCT_NAME;

-- CALCULATE POINTS BASED ON AMOUNT SPENT
-- IF PRODUCT IS SUSHI, APPLY 2X MULTIPLIER (20 POINTS PER $1), OTHERWISE 10 POINTS PER $1
WITH CTE AS (
    SELECT
        S.CUSTOMER_ID,
        M.PRODUCT_NAME,
        SUM(M.PRICE) AS AMOUNT_SPENT
    FROM
        SALES AS S
        INNER JOIN MENU AS M ON S.PRODUCT_ID = M.PRODUCT_ID
    GROUP BY
        S.CUSTOMER_ID,
        M.PRODUCT_NAME
)
SELECT
    *,
    CASE
        WHEN PRODUCT_NAME = 'SUSHI' THEN AMOUNT_SPENT * 20
        ELSE AMOUNT_SPENT * 10
    END AS POINTS
FROM
    CTE;

-- SUM UP POINTS FOR EACH CUSTOMER
WITH CTE2 AS (
    WITH CTE AS (
        SELECT
            S.CUSTOMER_ID,
            M.PRODUCT_NAME,
            SUM(M.PRICE) AS AMOUNT_SPENT
        FROM
            SALES AS S
            INNER JOIN MENU AS M ON S.PRODUCT_ID = M.PRODUCT_ID
        GROUP BY
            S.CUSTOMER_ID,
