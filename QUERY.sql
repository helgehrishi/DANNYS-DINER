    )
    SELECT
        *,
        CASE
            WHEN PRODUCT_NAME = 'SUSHI' THEN AMOUNT_SPENT * 20
            ELSE AMOUNT_SPENT * 10
        END AS POINTS
    FROM
        CTE
)
SELECT
    CUSTOMER_ID,
    SUM(POINTS) AS TOTAL_POINTS
FROM
    CTE2
GROUP BY
