-- 10. IN THE FIRST WEEK AFTER A CUSTOMER JOINS THE PROGRAM (INCLUDING THEIR JOIN DATE),
-- THEY EARN 2X POINTS ON ALL ITEMS, NOT JUST SUSHI. THIS QUERY CALCULATES HOW MANY POINTS
-- SELECT CUSTOMER_ID TO IDENTIFY WHICH CUSTOMER'S POINTS ARE BEING CALCULATED
-- CUSTOMERS A AND B HAVE EARNED BY THE END OF JANUARY.
-- IF ORDER DATE IS WITHIN THE FIRST WEEK AFTER JOIN DATE (INCLUSIVE)
-- OTHERWISE, CALCULATE POINTS NORMALLY
-- CALCULATE THE TOTAL POINTS FOR EACH CUSTOMER
-- JOIN MEMBERS TABLE TO ACCESS JOIN DATE FOR POINTS CALCULATION
-- JOIN MENU TABLE TO ACCESS ITEM PRICES
-- INCLUDE ONLY PURCHASES MADE ON OR AFTER THE JOIN DATE
-- INCLUDE ONLY PURCHASES MADE ON OR BEFORE THE END OF JANUARY
-- GROUP RESULTS BY CUSTOMER TO SUM POINTS PER CUSTOMER
SELECT
    S.CUSTOMER_ID,
    SUM(
        CASE
            WHEN S.ORDER_DATE BETWEEN ME.JOIN_DATE
            AND DATE_ADD(ME.JOIN_DATE, INTERVAL 7 DAY) THEN M.PRICE * 20
            ELSE M.PRICE * 10
        END
    ) AS TOTAL_POINTS
FROM
    SALES AS S
    INNER JOIN MEMBERS AS ME ON S.CUSTOMER_ID = ME.CUSTOMER_ID
    INNER JOIN MENU AS M ON S.PRODUCT_ID = M.PRODUCT_ID
WHERE
    S.ORDER_DATE >= ME.JOIN_DATE
    AND S.ORDER_DATE <= '2021-01-31'
GROUP BY
    S.CUSTOMER_ID
ORDER BY
    S.CUSTOMER_ID;

